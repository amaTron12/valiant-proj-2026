import { contextBridge, ipcRenderer } from 'electron'

contextBridge.exposeInMainWorld('api', {
  getClaims: () => ipcRenderer.invoke('get-claims'),
  createClaim: (data: unknown) => ipcRenderer.invoke('create-claim', data),
  updateClaim: (id: string, data: unknown) => ipcRenderer.invoke('update-claim', id, data),
  deleteClaim: (id: string) => ipcRenderer.invoke('delete-claim', id),
  restoreClaim: (id: string) => ipcRenderer.invoke('restore-claim', id),
  getClients: () => ipcRenderer.invoke('get-clients'),
  createClient: (data: unknown) => ipcRenderer.invoke('create-client', data),
  updateClient: (id: string, data: unknown, prevMatch: unknown) => ipcRenderer.invoke('update-client', id, data, prevMatch),
  deleteClient: (id: string) => ipcRenderer.invoke('delete-client', id),
  restoreClient: (id: string) => ipcRenderer.invoke('restore-client', id),
  getPets: () => ipcRenderer.invoke('get-pets'),
  createPet: (data: unknown) => ipcRenderer.invoke('create-pet', data),
  updatePet: (id: string, data: unknown, prevMatch: unknown) => ipcRenderer.invoke('update-pet', id, data, prevMatch),
  deletePet: (id: string) => ipcRenderer.invoke('delete-pet', id),
  restorePet: (id: string) => ipcRenderer.invoke('restore-pet', id),
  getClaimImages: (claimId: string) => ipcRenderer.invoke('get-claim-images', claimId),
  pickClaimImages: (claimId: string, docType: string | undefined) => ipcRenderer.invoke('pick-claim-images', claimId, docType),
  deleteClaimImage: (id: string) => ipcRenderer.invoke('delete-claim-image', id),
  restoreClaimImage: (id: string) => ipcRenderer.invoke('restore-claim-image', id),
  gdriveListFolders: (query?: string) => ipcRenderer.invoke('gdrive-list-folders', query),
  gdriveListFolderFiles: (folderId: string) => ipcRenderer.invoke('gdrive-list-folder-files', folderId),
  gdriveDownloadFile: (fileId: string) => ipcRenderer.invoke('gdrive-download-file', fileId),
  gdriveSaveFolder: (folderId: string, folderName: string) => ipcRenderer.invoke('gdrive-save-folder', folderId, folderName),
  gdriveGetFolder: () => ipcRenderer.invoke('gdrive-get-folder'),
  gdriveSaveCreds: (clientId: string, clientSecret: string) => ipcRenderer.invoke('gdrive-save-creds', clientId, clientSecret),
  gdriveStatus: () => ipcRenderer.invoke('gdrive-status'),
  gdriveConnect: () => ipcRenderer.invoke('gdrive-connect'),
  gdriveDisconnect: () => ipcRenderer.invoke('gdrive-disconnect'),
  gdriveListFiles: (query?: string) => ipcRenderer.invoke('gdrive-list-files', query),
  gdriveOpenFile: (webViewLink: string) => ipcRenderer.invoke('gdrive-open-file', webViewLink),
  gdriveLinkFile: (data: { id: string; claim_id: string; file_id: string; file_name: string; web_view_link: string; mime_type?: string }) => ipcRenderer.invoke('gdrive-link-file', data),
  gdriveGetLinks: (claimId: string) => ipcRenderer.invoke('gdrive-get-links', claimId),
  gdriveUnlinkFile: (id: string) => ipcRenderer.invoke('gdrive-unlink-file', id),
  getDiagnosisTypes: () => ipcRenderer.invoke('get-diagnosis-types'),
  getDeletedDiagnosisTypes: () => ipcRenderer.invoke('get-deleted-diagnosis-types'),
  createDiagnosisType: (data: unknown) => ipcRenderer.invoke('create-diagnosis-type', data),
  updateDiagnosisType: (id: string, data: unknown) => ipcRenderer.invoke('update-diagnosis-type', id, data),
  deleteDiagnosisType: (id: string) => ipcRenderer.invoke('delete-diagnosis-type', id),
  restoreDiagnosisType: (id: string) => ipcRenderer.invoke('restore-diagnosis-type', id),
  getPremiumPlans: () => ipcRenderer.invoke('get-premium-plans'),
  getDeletedPremiumPlans: () => ipcRenderer.invoke('get-deleted-premium-plans'),
  createPremiumPlan: (data: unknown) => ipcRenderer.invoke('create-premium-plan', data),
  updatePremiumPlan: (id: string, data: unknown) => ipcRenderer.invoke('update-premium-plan', id, data),
  deletePremiumPlan: (id: string) => ipcRenderer.invoke('delete-premium-plan', id),
  restorePremiumPlan: (id: string) => ipcRenderer.invoke('restore-premium-plan', id),
})
